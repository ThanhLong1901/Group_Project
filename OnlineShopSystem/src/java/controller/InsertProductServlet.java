    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
         response.sendRedirect("insertproduct.jsp");
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.setCharacterEncoding("UTF-8");
        String name = request.getParameter("name");
        String price = request.getParameter("price");
        String image = request.getParameter("image");
//        String image = uploadFile(request);
        String desc = request.getParameter("desc");
        String cate = request.getParameter("cate");
        String quantity = request.getParameter("quantity");
        String create = request.getParameter("create");
        String isActive_raw = request.getParameter("isActive");
        String isSale_raw = request.getParameter("isSale");
        String discount = request.getParameter("discount");
        ProductDAO pd = new ProductDAO();
        pd.insertProduct(name, price, image, desc, cate, quantity, discount, create, isSale_raw, isActive_raw);
        response.sendRedirect("listproduct");
    }

